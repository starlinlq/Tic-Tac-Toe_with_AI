class Complex {

    private double real;
    private double imaginary;

    public void setReal(double real){
        this.real = real;
    }
    public void setImaginary(double imaginary){
        this.imaginary = imaginary;
    }
    public double getReal(){
        return this.real;
    };
    public double getImaginary(){
        return this.imaginary;
    }
}